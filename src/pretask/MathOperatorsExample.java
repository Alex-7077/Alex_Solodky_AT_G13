package pretask;

public class MathOperatorsExample {
    public static void main(String[] args) {
        int g = 9;
        int a = 3;
        int i = g + a;
        int t = g - a;
        int u = g * a;
        int l = g / a;
        System.out.println("сложение =" + i);
        System.out.println("вычитание =" + t);
        System.out.println("умножение =" + u);
        System.out.println("деление =" + l);

    }
}
